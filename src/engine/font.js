const GLYPH_W = 5;
const GLYPH_H = 7;
const SPACING = 1;

export class Font {
  constructor() {
    // Pre-bake each glyph into its own canvas at white; we tint when drawing.
    this._cache = {};
    for (const [ch, rows] of Object.entries(G)) {
      const c = document.createElement('canvas');
      c.width = GLYPH_W;
      c.height = GLYPH_H;
      const ctx = c.getContext('2d');
      ctx.fillStyle = '#fff';
      for (let y = 0; y < GLYPH_H; y++) {
        const row = rows[y];
        for (let x = 0; x < GLYPH_W; x++) {
          if (row[x] === '#') ctx.fillRect(x, y, 1, 1);
        }
      }
      this._cache[ch] = c;
    }
  }

  glyphFor(ch) {
    const upper = ch.toUpperCase();
    return this._cache[upper] || this._cache['?'];
  }

  draw(ctx, str, x, y, color = '#f0f0e8') {
    // Tinting via globalCompositeOperation: draw glyph then color over with source-in.
    let cx = x;
    for (const ch of str) {
      const g = this.glyphFor(ch);
      // Use a tint canvas
      const t = this._tintCanvas(g, color);
      ctx.drawImage(t, cx, y);
      cx += GLYPH_W + SPACING;
    }
  }

  // Cache tinted glyph canvases
  _tintCanvas(g, color) {
    const key = color;
    if (!g._tints) g._tints = {};
    if (g._tints[key]) return g._tints[key];
    const c = document.createElement('canvas');
    c.width = g.width;
    c.height = g.height;
    const ctx = c.getContext('2d');
    ctx.drawImage(g, 0, 0);
    ctx.globalCompositeOperation = 'source-in';
    ctx.fillStyle = color;
    ctx.fillRect(0, 0, c.width, c.height);
    g._tints[key] = c;
    return c;
  }

  measure(str) {
    return str.length * (GLYPH_W + SPACING) - SPACING;
  }

  lineHeight() {
    return GLYPH_H + 2;
  }

  // Word-wrap a string to a maximum pixel width; returns array of lines.
  wrap(str, maxW) {
    const words = str.split(/\s+/);
    const lines = [];
    let cur = '';
    for (const w of words) {
      const next = cur ? cur + ' ' + w : w;
      if (this.measure(next) > maxW && cur) {
        lines.push(cur);
        cur = w;
      } else {
        cur = next;
      }
    }
    if (cur) lines.push(cur);
    return lines;
  }
}
