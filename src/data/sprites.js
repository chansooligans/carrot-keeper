function rabbitSide(outline, body, light, accent, facingLeft = false) {
  const rows = [
    "................",
    "....@@..........",
    "....@B@.........",
    "....@B@.........",
    "....@BL.........",
    "...@@@@.........",
    "..@BBBBBB@@.....",
    "..@BLBBBBBB@....",
    "..@B@BBBBLBB@...",
    "..@BBBLLLLBBB@..",
    "..@AAAAAAAAAA@..",
    "..@AAAAAAAAAA@..",
    "..@BBBLLLLBBB@..",
    "..@BB@@@@@@BB@..",
    "...@@......@@...",
    "................",
  ];
  const t = (s) => s
    .replaceAll('@', outline)
    .replaceAll('B', body)
    .replaceAll('L', light)
    .replaceAll('A', accent);
  if (facingLeft) return rows.map(r => r.split('').reverse().join('')).map(t);
  return rows.map(t);
}

// Walk frame: same as standing but feet alternate (shift one foot)
function rabbitWalk(outline, body, light, accent, dir) {
  const base = dir === 'down' ? rabbitDown(outline, body, light, accent)
    : dir === 'up' ? rabbitUp(outline, body, light, accent)
    : dir === 'left' ? rabbitSide(outline, body, light, accent, true)
    : rabbitSide(outline, body, light, accent, false);
  // Shift the bottom row to fake a step
  return base.slice(0, 14).concat([
    base[15], base[14],
  ]);
}

