// Helper: build a generic rabbit silhouette parameterized by colors.
// outline=k, body=B, body-light=L, accent=A (bandana/scarf), eye=k.
function rabbitDown(outline, body, light, accent) {
  const t = (s) => s
    .replaceAll('@', outline)
    .replaceAll('B', body)
    .replaceAll('L', light)
    .replaceAll('A', accent);
  return [
    "................",
    "....@@....@@....",
    "....@B@..@B@....",
    "....@B@..@B@....",
    "....@BL..@BL....",
    "...@@@@..@@@@...",
    "..@BBBBBBBBBB@..",
    "..@BLBBBBBBLB@..",
    "..@B@BBBBBB@B@..",
    "..@BBBLLLLBBB@..",
    "..@AAAAAAAAAA@..",
    "..@AAAAAAAAAA@..",
    "..@BBBBLLBBBB@..",
    "..@BB@@@@@@BB@..",
    "...@@.....@@@...",
    "................",
  ].map(t);
}

